import os
import telebot

TOKEN = os.environ.get('BOT_TOKEN')
if TOKEN is None:
    print("Ошибка: токен не найден")
    exit()

bot = telebot.TeleBot(TOKEN)

@bot.message_handler(commands=['start'])
def start(message):
    bot.send_message(message.chat.id, "👋 Дарова! Я Традиционный Бот. Спрашивай про скинхедов, карланов, бренды.")

@bot.message_handler(func=lambda message: True)
def handle_message(message):
    text = message.text.lower()

    if 'скинхед' in text:
        bot.reply_to(message, """🇬🇧 **Как зародилась субкультура скинхедов:**

**1960-е годы, Великобритания:**
• Субкультура зародилась среди рабочих парней
• На тот момент **не существовало скинхедов-нацистов** или бонхедов
• Были **традиционные скинхеды**
• Их мотив: работа, никакого буржуйства
• Они были против буржуазии

**1980-е годы:**
• Экономический кризис в Великобритании
• Большой приплыв мигрантов в страну
• Появились **бонхеды** (скинхеды-нацисты)
• Бонхеды обвиняли во всем мигрантов и массово были против них
• Произошел раскол субкультуры на традиционных и наци-скинхедов""")

    elif 'карлан' in text:
        bot.reply_to(message, """**Дарова, слушай меня!** 🤝

**Карланы** — это тот вид людей, которые пытаются влиться в субкультуру:

• 👕 Носят **паленную** (поддельную) одежду
• 📚 **Не знают историю** субкультур
• ❓ При спросе **не знают историю бренда**
• 🤷 **Не могут разъяснить** за ношение бренда

Это позеры, которые только делают вид, что "в теме", но на самом деле просто копируют внешний вид без понимания сути.""")

    elif 'тор штайнер' in text or 'thor steinar' in text or 'тш' in text:
        bot.reply_to(message, """👕 **Thor Steinar — разбор бренда:**

**Название:**
• Thor (Тор) — скандинавский бог грома и молнии
• Steinar (Стейнар) — распространенное скандинавское имя
• Отсылка к скандинавской мифологии и культуре

**Символика:**
• Изначально использовал руны и скандинавские символы
• Некоторые символы были заимствованы из германской мифологии
• В начале 2000-х бренд ассоциировался с правыми движениями

**Связь со скинхедами:**
• Бренд стал популярен среди определенных групп скинхедов
• Из-за символики и названия приобрел политический подтекст

**Почему НЕ аполитичный:**
• Сам бренд заявляет, что дистанцируется от политики
• Но из-за истории и аудитории воспринимается неоднозначно
• В Германии были судебные разбирательства по поводу символики""")

    elif 'лонсдейл' in text or 'lonsdale' in text or 'лонс' in text:
        bot.reply_to(message, """🥊 **Lonsdale — разбор бренда:**

**Название:**
• Назван в честь **графа Лонсдейла** (Hugh Cecil Lowther, 5th Earl of Lonsdale)
• Граф учредил пояса для бокса — "Lonsdale Belt"
• Бренд основан в **1960 году** в Лондоне

**Символика:**
• Логотип — корона и боксерские перчатки
• Изначально чисто спортивный бренд для бокса
• Британская символика, отсылка к аристократии и спорту

**Связь со скинхедами:**
• В 1970-80х стал популярен среди скинхедов и футбольных хулиганов
• Из-за букв NSL (когда куртку застегивали, оставались буквы NS — National Socialism)
• Сами скинхеды прикалывались: "Lonsdale — расшифровывается как Let Only National Socialists Dress Accordingly"

**Почему аполитичный:**
• Бренд всегда дистанцировался от политики
• Поддерживает бокс и спорт по всему миру
• Негативно относится к использованию символики в политических целях
• Официально осуждает экстремизм

**Связь с субкультурой:**
• Носят за удобство и классический стиль
• Многие традиционные скинхеды носят Lonsdale как дань истории, без политики""")

    elif 'стоун айленд' in text or 'stone island' in text or 'стоник' in text:
        bot.reply_to(message, """🧥 **Stone Island — разбор бренда:**

**Название:**
• "Каменный остров" — отсылка к роману Джеймса Джойса
• Основан в **1982 году** в Италии дизайнером Массимо Ости
• Изначально подразделение C.P. Company

**Символика:**
• Знаменитая нашивка-компас (роза ветров) с красной/зеленой/синей точкой
• Компас символизирует **исследования и путешествия**
• Нашивка меняет цвет в зависимости от коллекции
• Никакой политической символики изначально не было

**Технологии:**
• Первый бренд, начавший использовать инновационные ткани
• Ткань "Tela Stella" — пропитанный нейлон, меняющий цвет и фактуру
• Термохромные ткани, меняющие цвет от температуры

**Связь со скинхедами:**
• Стал популярен в 1980-90х среди футбольных хулиганов и скинхедов
• Но чаще всего **ассоциируется с casuals** (околофутбольная культура)
• Из-за дороговизны стал символом статуса

**Почему аполитичный:**
• Бренд чисто про **технологии и моду**
• Никакой политической повестки
• Компас — это про морскую тематику и исследования, а не политику""")

    elif 'аполитичн' in text:
        bot.reply_to(message, """**Аполитичность брендов:**

✅ **Аполитичные:**
• **Stone Island** — чисто про технологии и моду
• **Lonsdale** — спортивный бренд, осуждает экстремизм
• **Fred Perry** — теннисные поло, хотя их тоже носят разные субкультуры
• **Alpha Industries** — военная эстетика, но без политики

⚠️ **Неоднозначные:**
• **Thor Steinar** — изначальная символика вызывала вопросы
• **Pitbull** — немецкий бренд, который ассоциировался с правыми

**Главное правило:**
Бренд — это просто одежда. Политику в него привносят люди, которые его носят.""")

    else:
        bot.reply_to(message, "Напиши: скинхеды, карланы, thor steinar, lonsdale, stone island, аполитичные бренды")

print("✅ Бот запущен с подробными ответами!")
bot.polling(none_stop=True)
