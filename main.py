import os
import telebot
import time

TOKEN = os.environ.get('BOT_TOKEN')
if TOKEN is None:
    print("❌ Ошибка: Токен не найден!")
    exit()

bot = telebot.TeleBot(TOKEN)

@bot.message_handler(commands=['start'])
def start(message):
    welcome_text = """👋 **Дарова! Я Традиционный Бот**

Я расскажу тебе:
• Историю субкультур
• Кто такие карланы
• Символику брендов и их связь со скинхедами

Просто задай вопрос!"""
    bot.send_message(message.chat.id, welcome_text)

@bot.message_handler(commands=['help'])
def help(message):
    help_text = """**Что можно спросить:**

• Как зародилась субкультура скинхедов?
• Кто такие карланы?
• Thor Steinar (что означает символика?)
• Lonsdale (связь со скинхедами)
• Stone Island (почему носят?)
• Какие бренды аполитичны?"""
    bot.send_message(message.chat.id, help_text)

@bot.message_handler(func=lambda message: True)
def handle_message(message):
    text = message.text.lower()
    
    # ===== ВОПРОС ПРО СКИНХЕДОВ =====
    if 'скинхед' in text or 'зародилась субкультура' in text or 'история скинхедов' in text:
        answer = """🇬🇧 **Как зародилась субкультура скинхедов:**

**1960-е годы, Великобритания:**
• Субкультура зародилась среди рабочих парней
• На тот момент **не существовало скинхедов-нацистов** или бонхедов
• Были **традиционные скинхеды**
• Их мотив: работа, никакого буржуйства
• Они были против буржуазии

**1980-е годы:**
• Экономический кризис в Великобритании
• Большой приплыв мигрантов в страну
• Появились **бонхеды** (скинхеды-нацисты)
• Бонхеды обвиняли во всем мигрантов и массово были против них
• Произошел раскол субкультуры на традиционных и наци-скинхедов"""
        bot.reply_to(message, answer)
    
