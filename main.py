import os
import telebot

TOKEN = os.environ.get('BOT_TOKEN')
if TOKEN is None:
    print("Ошибка: токен не найден")
    exit()

bot = telebot.TeleBot(TOKEN)

@bot.message_handler(commands=['start'])
def start(message):
    text = """👋 Дарова! Я Традиционный Бот

Я расскажу тебе:
• Историю субкультур
• Кто такие карланы
• Символику брендов

Просто напиши вопрос."""
    bot.send_message(message.chat.id, text)

@bot.message_handler(func=lambda message: True)
def handle_message(message):
    text = message.text.lower()

    # ===== СКИНХЕДЫ =====
    if 'скинхед' in text:
        answer = """🇬🇧 История скинхедов:

1960-е, Великобритания:
• Субкультура зародилась среди рабочих парней
• Не было скинхедов-нацистов
• Были традиционные скинхеды
• Они были против буржуазии

1980-е:
• Экономический кризис
• Приплыв мигрантов
• Появились бонхеды (нацисты)
• Они обвиняли во всем мигрантов
• Субкультура раскололась"""
        bot.reply_to(message, answer)

    # ===== КАРЛАНЫ =====
    elif 'карлан' in text:
        answer = """Дарова, слушай меня! 🤝

Карланы — это позеры, которые:
• Носят паленую (поддельную) одежду
• Не знают историю субкультур
• При спросе не могут пояснить за бренд
• Просто копируют внешний вид без понимания"""
        bot.reply_to(message, answer)

    # ===== THOR STEINAR =====
    elif 'тор штайнер' in text or 'thor steinar' in text or 'тш' in text:
        answer = """👕 Thor Steinar — разбор бренда:

Название:
• Thor (Тор) — скандинавский бог грома
• Steinar — скандинавское имя
• Отсылка к скандинавской мифологии

Символика:
• Использовал руны и скандинавские символы
• В начале 2000-х ассоциировался с правыми

Связь со скинхедами:
• Стал популярен среди определенных групп

Почему не аполитичный:
• Бренд заявляет об аполитичности
• Но из-за истории воспринимается неоднозначно"""
        bot.reply_to(message, answer)

    # ===== LONSDALE =====
    elif 'лонсдейл' in text or 'lonsdale' in text or 'лонс' in text:
        answer = """🥊 Lonsdale — разбор бренда:

Название:
• В честь графа Лонсдейла
• Он учредил пояса для бокса
• Бренд основан в 1960 году в Лондоне

Символика:
• Логотип — корона и боксерские перчатки
• Изначально спортивный бренд

Связь со скинхедами:
• В 70-80х стал популярен среди скинхедов
• Из-за букв NSL (когда куртку застегивали)
• Сами скинхеды шутили про расшифровку

Почему аполитичный:
• Бренд дистанцируется от политики
• Поддерживает бокс по всему миру
• Осуждает экстремизм"""
        bot.reply_to(message, answer)

    # ===== STONE ISLAND =====
    elif 'стоун айленд' in text or 'stone island' in text or 'стоник' in text:
        answer = """🧥 Stone Island — разбор бренда:

Название:
• "Каменный остров"
• Отсылка к роману Джеймса Джойса
• Основан в 1982 году в Италии

Символика:
• Нашивка-компас (роза ветров)
• Компас символизирует исследования
• Никакой политики

Технологии:
• Инновационные ткани
• Ткань меняет цвет от температуры
• Каждая вещь тестируется

Связь со скинхедами:
• Популярен среди футбольных фанатов
• Но чаще ассоциируется с casuals

Почему аполитичный:
• Бренд про технологии и моду
• Никакой политической повестки"""
        bot.reply_to(message, answer)

    # ===== АПОЛИТИЧНОСТЬ =====
    elif 'аполитичн' in text:
        answer = """Аполитичные бренды:

✅ Stone Island — технологии и мода
✅ Lonsdale — спорт, осуждает экстремизм
✅ Fred Perry — теннис, хотя носят разные
✅ Alpha Industries — военная эстетика

⚠️ Неоднозначные:
• Thor Steinar — из-за символики

Главное:
Бренд — это просто одежда.
Политику привносят люди."""
        bot.reply_to(message, answer)

    # ===== ЕСЛИ НИЧЕГО НЕ ПОНЯЛ =====
    else:
        bot.reply_to(message, "Напиши: скинхеды, карланы, thor steinar, lonsdale, stone island, аполитичные бренды")

print("✅ Бот запущен!")
bot.polling(none_stop=True)
